package main

import (
	"fmt"
)

// Функція для пошуку унікальних елементів у масиві
func знайтиУнікальніЕлементи(масив []int) []int {
	унікальні := []int{}
	елементи := make(map[int]bool)

	for _, значення := range масив {
		if !елементи[значення] {
			елементи[значення] = true
			унікальні = append(унікальні, значення)
		}
	}
	return унікальні
}

func main() {
	масив := []int{1, 2, 3, 2, 4, 5, 3, 6}
	fmt.Println("Унікальні елементи:", знайтиУнікальніЕлементи(масив))
}
