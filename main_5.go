package main

import (
	"fmt"
)

// Функція для генерації таблиці множення
func згенеруватиТаблицюМноження(число, ліміт int) {
	for i := 1; i <= ліміт; i++ {
		fmt.Printf("%d x %d = %d\n", число, i, число*i)
	}
}

func main() {
	var число, ліміт int
	fmt.Print("Введіть число: ")
	fmt.Scan(&число)
	fmt.Print("Введіть ліміт: ")
	fmt.Scan(&ліміт)
	fmt.Println("Таблиця множення:")
	згенеруватиТаблицюМноження(число, ліміт)
}
